function walk(grid, start, end) {
  let date = Date.now();
  let visited = [];
  let frontier = [];
  let path = {};
  let find = false;
  frontier.unshift(start);
  visited.push(start[0] + ';' + start[1]);
  path[start[0] + ';' + start[1]] = { x: start[0], y: start[1], originX: start[0], originY: start[1] };

  while (frontier.length > 0 && find != true) {
    let frontierItem = frontier.shift();
    let array = [];

    for (let dir of getDirectionsFromHeuristic(frontierItem, end)) {
      let x = frontierItem[0] + dir[0];
      let y = frontierItem[1] + dir[1];

      if (grid[y] == null || grid[y][x] == null || grid[y][x] == 1 || visited.includes(x + ';' + y)) {
        continue;
      }

      if (x == end[0] && y == end[1]) {
        find = true;
      }

      visited.push(x + ';' + y);
      array.push([x, y]);
      path[x + ';' + y] = { x: x, y: y, originX: frontierItem[0], originY: frontierItem[1] };
    }

    frontier.push(...array);
  }

  let final = [];
  let p = path[end[0] + ';' + end[1]];

  while (true) {
    final.unshift({ x: p.x, y: p.y });
    p = path[p.originX + ';' + p.originY];
    if (p.x == start[0] && p.y == start[1]) {
      break;
    }
  }
  console.log(Date.now()- date);
  drawPath(final)
  drawCase(start[0], start[1], 0);
  return final;
}

function getDirectionsFromHeuristic(p1, p2) {
  let directions = [
    [+0, -1],
    [+1, +0],
    [+0, +1],
    [-1, +0]
  ];

  let dx = p2[0] - p1[0];
  let dy = p2[1] - p1[1];

  return directions.sort((a, b) => {
    let lengthA = Math.sqrt((dx - a[0]) * (dx - a[0]) + (dy - a[1]) * (dy - a[1]));
    let lengthB = Math.sqrt((dx - b[0]) * (dx - b[0]) + (dy - b[1]) * (dy - b[1]));
    return lengthA - lengthB;      
  });
}

function drawTerrain(data) {
  const canvas = document.querySelector("canvas");
  const ctx = canvas.getContext('2d');

  let ref = grid.length > grid[0].length ? grid.length*size : grid[0].length*size;
  
  canvas.height = ref;
  canvas.width = ref;
  
  const grass = ["#248224", "#0a750a"];
  const wall = "#5d715d";
  
  ctx.fillStyle = grass[0];
  
  let grassSwitch = 0;
  
  for(const tableIndex in data){
    
    for(const boxIndex in data[tableIndex]){
      
      const posX = boxIndex*size;
      const posY = tableIndex*size;
      
      const box = data[tableIndex][boxIndex];
      grassSwitch = (grassSwitch==0)? 1 : 0;
      
      if(box == 0) {
        ctx.fillStyle = grass[grassSwitch];
      } else {
        ctx.fillStyle = wall;
      }
      
      ctx.fillRect(posX, posY, size, size);
    }
  }
  ctx.stroke();
}
function drawCase(indX, indY, type = 0){
  const canvas = document.querySelector("canvas");
  const ctx = canvas.getContext('2d');
  
  const color = [
    "#6abe30",
    "#fbf236",
    "#ac3232"
  ]
  
  let grassSwitch = 0;
      
  const posX = indX*size;
  const posY = indY*size;
  
  ctx.fillStyle = color[type];

  ctx.fillRect(posX, posY, size, size);
  
  ctx.stroke();
}

function drawPath(path){
  
  if(path === undefined) return;
  path.forEach((data, key)=>{

    let color = (path.length-1 === key)? 2:1;
    if(data.length === 0) return;

    drawCase( data["x"], data["y"], color);
  })
}


